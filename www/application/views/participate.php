<h1>¿Cuáles son las categorías?</h1>
<br/>
<ul>
	<li>Alteración de urnas: robo, quema, destrucción, etc de las urnas. <strong>(ALTE)</strong></li>
<li>Padrón Incompleto: el nombre del votante no aparece en el padrón en casilla. <strong>(PADR)</strong></li>
<li>Violación del voto secreto: no se le permite a las y los votantes mantener su voto en secreto. <strong>(SECR)</strong></li>
<li>Falsificación de resultados: los votos contados son diferentes a los votos reportados en las actas de la casilla. <strong>(FALS)</strong></li>
<li>Campañas en casilla: hay personas haciendo campaña por partidos o candidatos dentro de la casilla. <strong>(CAMP)</strong></li>
<li>Violencia: hay actos de violencia contra los funcionarios de casilla o los votantes. <strong>(VIOL)</strong></li>
<li>Credenciales Falsas: se están usando credenciales de elector falsificadas para votar ilegalmente. <strong>(CRED)</strong></li>
<li>Retención de Credencial: hay una persona o personas acumulando y reteniendo credenciales con el objetivo de presionar a los votantes. <strong>(RETE)</strong></li>
<li>Compra de votos: hay personas ofreciendo un pago en dinero o en especie a cambio de que se vote por cierta persona o partido. <strong>(COMP)</strong></li>
<li>Funcionarios irregulares: Fueron sustituidos los funcionarios de casilla designados por el IFE de manera irregular <strong>(SUST)</strong></li>
<li>Coacción sindical: coerción y proselitismo sindical, se identificaron grupos corporativos o sindicales acarreando o sustituyendo votantes <strong>(SIND)</strong></li>
<li>Coacción con programas sociales: se usan programas sociales por parte de funcionarios públicos o de partidos para amenazar a votantes y obligarlos a votar por un partido o candidato <strong>(PROG)</strong></li>
	</li>
</ul>
<br/><br/>
<h1>¿Cuáles son nuestras fuentes de información?</h1>
<br/>
El mapa de "Cuidemos el Voto" distinguirá dos tipos de fuentes de información distinta:
<br/><br/>
<ol>
	<li>La validada por observadores electorales previamente registrados ante el IFE. Ésta aparecerá como información verificada.</li>
	<li>La abierta, que será enviada por cualquier ciudadano o generada a partir de fuentes noticiosas. Ésta aparecerá como información no verificada.</li>
</ol>
<br/><br/>
<h1>Cualquier persona comete un delito electoral si:</h1>
<br/>
<ol>
<li>Te ofrece comprar tu voto.</li>
<li>Presiona o induce en la casilla a los electores para votar por un partido o candidato.</li>
<li>Pretende votar con la credencial de elector de otra persona.</li>
<li>Introduce o sustrae ilícitamente boletas electorales de las urnas.</li>
<li>Recoge credenciales de elector.</li>
</ol>
Estos delitos pueden ser sancionados con pena de cárcel.
<br/><br/>
<h1>Los funcionarios electorales cometen un delito electoral si: </h1>
<br/>
<ol>
<li>Impiden la entrega oportuna de documentos o materiales electorales.
<li>Abren o cierran una casilla fuera de tiempo, sin justificación.</li>
<li>Alteran los resultados electorales.</li>
<li>Presionan a los electores para votar por un partido o candidato.</li>
</ol>
<br/><br/>
<h1>Los funcionarios partidistas y candidatos cometen delitos si:</h1>
<br/
<ol>
<li>Obstaculizan el desarrollo normal de las votaciones.</li>
<li>Impiden con violencia la instalación, apertura o cierre de una casilla.</li>
<li>Publican noticias falsas sobre los resultados electorales.</li>
<li>Destruyen o hacen uso indebido del material electoral.</li>
<li>Realizan propaganda electoral desde 4 días antes de las elecciones.</li>
</ol>

<br/><br/>
<h1>Los servidores públicos comete un delito electoral si:</h1>
<br/>
<ol>
<li>Obligan a sus empleados a votar por un partido político o candidato.</li>
<li>Condicionan apoyo del gobierno a cambio de votos.</li>
<li>Utilizan recursos públicos para apoyar a un partido político o candidato.</li>
<li>Usan el tiempo de labores de sus empleados para apoyar a partidos o candidatos.</li>
</ol>

<br/><br/>
