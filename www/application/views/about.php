					<h1>Acerca de <i>Cuidemos el Voto</i></h1>
					<div>
Cuidemos el Voto es una plataforma independiente para contribuir al seguimiento de las elecciones federales del 5 de julio del 2009 en México. 
<br/><br/>
La información que recopila Cuidemos el Voto es generada a partir de mensajes de texto (SMS), emails, twitter, y reportes en un formulario en página web.
El objetivo principal es reportar incidentes e irregularidades en el proceso electoral que determina el <a href="http://www.pgr.gob.mx/fepade/cuales%20son%20los%20delitos%20electorales/cuales%20son%20los%20delitos%20electorales.asp" title="Código Penal Federal">Código Penal Federal</a> y el <a href="http://info4.juridicas.unam.mx/ijure/tcfed/4.htm?s=" title="Código Federal de Instituciones y Procedimientos Electorales">Código Federal de Instituciones y Procedimientos Electorales</a>. La plataforma de Cuidemos el Voto agrega estos reportes junto con información de notas de prensa, fotos y videos relacionadas a las elecciones en un mapa interactivo. 
<br/><br/>
Cuidemos el Voto utiliza un motor para la "gestión de crisis" llamado <a href="http://www.ushahidi.org/" title="Ushahidi">Ushahidi</a>. Ushahidi es un proyecto de código abierto desarrollado por programadores en Kenia, Ghana, Sudáfrica, Malawi, Holanda y Estados Unidos. 						
<br/><br/>
<h1>¿Quiénes somos?</h1>
<br/>
El equipo de Cuidemos el Voto es producto de un conjunto de redes e intercambios electrónicos que poco a poco se han sumado para producir este proyecto. 
<br/><br/>
<a href="mailto:oscar.salazar@cuidemoselvoto.org" title="Oscar Salazar">Oscar Salazar</a> (OS) manda un correo desde Nueva York con el interés de promover el uso de teléfonos celulares para hacer activismo social en México. Al mismo tiempo <a href="mailto:andres.lajous@cuidemoselvoto.org" title="Andrés Lajous">Andrés Lajous</a> (AL) en una clase de activismo social en redes celulares en <a href="http://www.mit.edu/" title="Massachusetts Institute of Technology">MIT</a> abre su celular para que cualquiera pueda actualizar su cuenta personal de Twitter. 
<br/><br/>
AL contesta el correo de OS, y OS el de AL. Acuerdan una fecha y hora para platicar por Skype. 
<br/><br/>
Intentan hablar por Skype se corta la llamada varias veces. Por fin tienen una conversación. OS es de Colima y estudió en Francia telecomunicaciones, AL es del Distrito Federal y estudia Planificación Urbana en Estados Unidos. OS tiene experiencia con las redes celulares en México, AL con las instituciones electorales mexicanas. Acuerdan intercambiar correos. AL quiere usar celulares y Twitter para hacer observación electoral, OS le muestra la experiencia de <a href="http://votereport.in/">Ushaidi en la India</a>. Se ponen de acuerdo para empezar a desarrollar el proyecto para las elecciones federales mexicanas del 2009. 
<br/><br/>
OS manda un correo a Jorge Soto (JS) en Monterrey y a <a href="http://oviedos.com.mx" title="José Oviedo">José Oviedo</a> (JO) en Colima para contarle del proyecto pues ellos tienen experiencia manejando servidores y servicios de SMS en celulares. AL le envía un correo a Maite Azuela (MA) y a Brando Flores (BF) ambos con experiencia tanto en observación electoral como en organizaciones políticas. JS, JO, MA y BF aceptar sumarse al proyecto. 
<br/><br/>
Nos damos cuenta que nos faltan dos cosas centrales: un nombre y el diseño gráfico. 
<br/><br/>
AL va a NY a conocer a OS en persona. Hablan de un par de propuestas. No están del todo convencidos. Se deciden sin pensar mucho por ¡Qué se cuenten los votos! OS comiendo y AL en un camión con Wi-Fi de regreso a Boston, se dan cuenta que lo mejor sería someter el nombre a votación abierta entre sus contacto de Facebook y Twitter. En <a href="http://selectricity.org/">Selectricity</a> crean una votación. De tres porpuestas 1) ¡Qué se cuenten los votos!, 2) ¡Protege tu voto! 3) ¡Cuidemos el voto! Tras 24 horas de votación, gana por orden de preferencia ¡Cuidemos el voto! 
<br/><br/>
Una vez que tenemos nombre, AL le manda un correo a <a href="mailto:black@mit.edu">Luis Blackaller</a> (LB) para proponerle hacer un logo y un banner para la página. LB manda las primeras muestras que transmiten lo que buscaban OS y AL con el nombre. LB manda sugerencias y modificaciones. JO las monta y adapta al interfaz. Eso es lo que ves cuando entras a nuestra página principal. 
<br/><br/>
Mientras tanto MA y BF empiezan a contactar a medios de comunicación y ONGs de observación electoral en México para presentarles la plataforma e invitarlos a usarla. 
<br/><br/>
En fechas más recientes, viendo el éxito de Twitter alrededor del mundo y en México. AL pone un tweet, preguntando si algún programador/a ofrece su tiempo para hacer un visualizador de temas electorales para Cuidemos el Voto. Vía mensaje directo <a href="mailto:maggit@maggit.com.mx" title="Raquel Hernández">Raquel Hernández</a> (RH), se apunta junto con <a href="mailto:damog@damog.net" title="David Moreno">David Moreno</a> (DM) para hacerlo. En un par de días junto con JO, DM y RH vuelven el visualizador parte del motor de Ushaidi. ¡Una contribución mexicana a la plataforma!
